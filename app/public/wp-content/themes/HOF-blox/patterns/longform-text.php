<!-- /wp:group -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">“So we talked for like two hours about baseball and never got to tennis until the end, and he said ‘why did you come here? You didn’t come here to talk about baseball.’ I said, ‘No, I didn’t.’ I sold him three years for 75 grand a year, which was fabulous. That made our year. That got us off the mark.”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">Sports stories? Jack could go on forever, if not for the fact that he gets tired easily these days and needs his rest.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">But you won’t hear him complain.&nbsp;</p>
