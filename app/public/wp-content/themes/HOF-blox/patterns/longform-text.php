<!-- wp:group {"style":{"spacing":{"blockGap":"1rem","padding":{"right":"14vw","left":"14vw"},"margin":{"top":"2rem","bottom":"2rem"}}},"className":"grafstack-cols","layout":{"type":"flex","orientation":"vertical","flexWrap":"wrap"}} -->
<div class="wp-block-group grafstack-cols" style="margin-top:2rem;margin-bottom:2rem;padding-right:14vw;padding-left:14vw"><!-- wp:paragraph -->
<p>&nbsp;<strong><em>What’s Up With … Jack Levitt&nbsp;</em></strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><em>In anticipation of the spring 2024 opening of the new St. Louis Tennis Hall of Fame at the Armory, 2016 inductee Ron Cobb is writing weekly “What’s Up With” features on Hall of Fame members and updates about the Hall of Fame.&nbsp;</em></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">In his heyday, Jack Levitt was a fabulous promoter, one of the best St. Louis has ever seen. He not only brought World Team Tennis and other tournaments to St. Louis, he worked in the front office of the St. Louis Hawks and, most impressive of all, he was the guy who started the annual Missouri-Illinois basketball series.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">But Jack is 91 now and not doing all that great. He’s in assisted living at McKnight Place.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">“I don’t know how much longer I’m gonna make it because I’m slipping fast,” he told me in early March. “I’ve still got my kids and my grandkids, and I’ve got a great-grandchild now.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">“But physically it’s really tough. I’ve got a lot of things wrong with me. The last two and a half years I’ve been in the hospital three times. But you know what? It’s OK. I’ve had a great life and I’ll never complain about it, that’s for sure.”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">Forget the stereotype of the widowed gentleman in a nursing home regaling a group of silver-haired ladies at the bingo table. Jack says he never leaves his room other than to drive his electric wheelchair down the hall to get a haircut. He takes all of his meals in his room.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">“My problem is that I don’t have any energy,” he said. “I’m very anemic. It just goes on and on. But you know, everybody’s going to go through it, hopefully, if you live long enough.”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">Jack counts himself lucky to have so many great memories of his years in sports. He ran the St. Louis Aces of WTT for 12 years – part of those years with Tom O’Neal. The Aces’ WTT championship in 1996 ranks high on Jack’s list of memories, and so does a night in Kansas City when, after a WTT match, he drove Venus Williams and the other players around in a rental van looking for a late-night place to eat.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">He’d almost given up hope when he saw a tavern with a sign in the window that said “open.”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">“I said ‘here we go guys. It’s got food.’ We get out, but Venus doesn’t. She says, ‘Mr. Jack, can you take me back to the hotel?’ I said, ‘Venus, don’t you want to eat?’ She said ‘I can’t go in&nbsp;there because they serve liquor.’ I said, ‘Oh, I’m sorry.’ Then I said to the other players, ‘You know what? We play as a team, we eat as a team. Everybody back in the car.’&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">“So we got in the van and found a Popeye’s. She said, ‘Oh, Mr. Jack, don’t you dare tell my father we ate at Popeye’s’ and I said, ‘Venus, my lips are sealed.’ So we pulled up tables and chairs in the hotel lobby and had Popeye’s. At about 2 in the morning, I said ‘everybody, I’ve got to go to bed. I’m tired.’ And Venus put her arm around me and gave me a big kiss. She said ‘I’ve never had so much fun in all my life,’ and I said ‘Venus, neither have I.’”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">Jack got a quick education in professional sports when he was in high school and got a job as clubhouse boy for the old Flyers hockey team and the Bombers basketball team at the Arena on Oakland Avenue.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">After high school, he was signed to play baseball in the Cleveland Indians organization. He was a promising shortstop until he wrecked his shoulder on a play at second base.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">“I played with all those big league players – Rocky Colavito, Herb Score … Roger Maris was a teammate in spring training,” he said.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">Jack got drafted into the military and spent two years playing Army baseball during the Korean War.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">In Cleveland, Jack met a PR guy named Marty Blake, who later became general manager of St. Louis’ new NBA team, the Hawks. Jack was invited to St. Louis and sat at a game next to Blake, who was also the public address announcer.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">“So we’re sitting there and Marty had to go somewhere and he said, ‘take over the PA.’ I didn’t know what the hell to do. Anyway, I took over the PA and did it for 12 years.”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">Jack, Blake and owner Ben Kerner were a three-man operation. Jack got the idea of putting on tennis exhibitions on the basketball floor after some of the Hawks’ Sunday afternoon games. “Butch (Buchholz) would play and got Laver, and I was one of the guys laying down the lines with tape.”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">In due time Jack was a full-time promoter and on the lookout for events that could turn a profit. He got the idea of a Missouri-Illinois basketball game.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">“I worked for years to get Norm Stewart to play in St. Louis. I said, ‘You can’t recruit in St. Louis if you don’t play here.’ He didn’t want to do it. You know, Norm wasn’t very nice sometimes.’”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">But Jack got the athletic directors on board and the annual series around Christmas week was born.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">“So the game took off and both sides have made tons of money. So that was my big accomplishment for Missouri.”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">Jack wasn’t a tennis player in those days, but his daughter, Susie, was. She played No. 1 singles at Ladue High when it won the state championship. (Susie’s daughter Annie later won a state doubles title on a Ladue team that won state.)&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">“I didn’t start playing until my mid-40s,” Jack said. “My kids started playing, so I said I’d better start to play so I could play with them. I fell in love with it because it kept me in shape. It’s one of the reasons I’ve lived to be 91.”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">Once he started promoting tennis events, Jack brought virtually all of the top players to St.&nbsp;Louis over the years. His events were largely successful, except for an ill-fated tournament at Kiel Auditorium in the fall of 1985 that went head-to-head with the Cardinals playing in the playoffs.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">He had a weeklong tournament headlined by Jimmy Connors and Ilie Nastase, and the two of them made it to the final.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">“They were good friends,” Jack said, “and in the final they were cussing at each other the whole time. It got pretty bad, but we had 11,000 people in the stands and that sold the whole event.” </p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":"1.5"}}} -->
<p style="line-height:1.5">Then along came World Team Tennis.&nbsp;</p>
<!-- /wp:paragraph -->

