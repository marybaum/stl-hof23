<p style="line-height:1.5">“Tom O’Neal got me interested. I was gonna run it and he would do the sales. But at first sales weren’t coming in, and I got an appointment with the president of United Van Lines. We started talking and it turns out he was a former farmhand of the Pittsburgh Pirates.&nbsp;</p>
<!-- /wp:paragraph -->

